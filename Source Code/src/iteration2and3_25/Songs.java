package iteration2and3_25;

public class Songs
{
    private String title;
    private int time;

    public Songs(String title, int time)
    {
    	this.title = title;
    	this.time = time;
    }

    public String getTitle()
    {
    	return title;
    }

    public int getTime()
    {
        return time;
    }
}